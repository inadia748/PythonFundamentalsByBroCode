# for loop  is a statement that will execute it 's block of code a limited number of times

'''
while loop = unlimited 
for loop = limited

'''


for i in range(10):
    print(i)
print('\n')
for i in range(10):
    print(i+1)
print('\n')
# range(50, 100), will go from 50 till 99
for i in range(50, 100):
    print(i)
print('\n')
# range(50, 101, 2) is like range(start, excluded, step)
for i in range(50, 100+1, 2):
    print(i)
print('\n')


for i in 'BroCode':
    print(i)



